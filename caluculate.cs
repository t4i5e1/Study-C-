using System;

namespace Example
{
    class Program
    {
        static void Main(string[] args)
        {
            int answer; //計算結果

            //加算
            answer = 3 + 4;
            Console.WriteLine(answer);

            //減算
            answer = 12 - 18;
            Console.WriteLine(answer);

            //乗算
            answer = 2 * 7;
            Console.WriteLine(answer);

            //除算
            answer = 18 / 3;
            Console.WriteLine(answer);

            //剰余
            answer = 21 % 5;
            Console.WriteLine(answer);
        }
    }
}
